name = "Harry"

# slicing


nameshort = name[0:3] # start from index 0 all the way till 3 (excluding 3)

print(nameshort)

character1 = name[1]

print(character1)

# negative slicing 

name = "Harry" 


print(name[-4:-1])
print(name[1:4]) 

# skip slicing 

a = "1234567890ABCDEFGH"
print(a[1:9:4])
print(a[1:5])
